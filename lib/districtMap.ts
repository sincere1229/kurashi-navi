// ============================================================
// 郡名マッピング(町村名 → 郡名)
// 「都道府県名+郡名」で検索するユーザー向けに、ガイドページのタイトル・本文に
// 正式な住所表記(例:埼玉県比企郡嵐山町)を含めるためのデータ。
//
// 全国の町村すべてを網羅するのは大規模作業のため、現時点では代表的な郡から
// 順に追加していく方針。キー:「都道府県+町村名」/値:郡名
// ============================================================

export const DISTRICT_MAP: Record<string, string> = {
  // 埼玉県比企郡
  "埼玉県嵐山町": "比企郡",
  "埼玉県小川町": "比企郡",
  "埼玉県川島町": "比企郡",
  "埼玉県吉見町": "比企郡",
  "埼玉県鳩山町": "比企郡",
  "埼玉県ときがわ町": "比企郡",
  "埼玉県東秩父村": "秩父郡", // 東秩父村は秩父郡(比企郡と誤認されやすいので明記)
};

// 「都道府県 + 市区町村」の正式なフルネームを返す(郡が分かる場合のみ郡を挟む)
export function fullAddress(pref: string, city: string): string {
  const district = DISTRICT_MAP[`${pref}${city}`];
  return district ? `${pref}${district}${city}` : `${pref}${city}`;
}
